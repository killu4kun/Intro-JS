a = 2;
b = 2;

selector = "Adição"

switch (selector) {
    case "Adição":
        console.log(a+b);
        break;
    case "Subtração":
        console.log(a-b);
        break;
    case "Divisão":
        console.log(a/b);
        break;
    case "Multiplicação":
        console.log(a*b);
        break;
    case "Módulo":
        console.log(a%b);
        break;    
    default:
        break;
}